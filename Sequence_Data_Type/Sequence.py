print("Demonstration of sequence data type.")

ldata = [10,20,30,40] # List data type.

tdata = (10,20,30,40) # Tuple data type.

sdata = {10,20,30,40,50} # set data type.

ddata = {"C":400,"C++":550,"Java":800,"Python":1000} # Dictionary data type.

print(ldata)
print(tdata)
print(sdata)
print(ddata)

print(type(ldata))
print(type(tdata))
print(type(sdata))
print(type(ddata))
